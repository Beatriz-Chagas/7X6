int	ft_sqrt(int nb)
{
	int	n;
	int	i;

	n = 0;
	i = 1;
	while (nb >= i)
	{
		nb = nb - i;
		i = i + 2;
		n = n + 1;
	}
	if (nb != 0)
		n = 0;
	return (n);
}
